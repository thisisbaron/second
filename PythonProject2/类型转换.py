#  + 表示两个元素相加
print('+ : 2 + 3的值是:', 2 + 3) # 5
#  - 表示两个元素相减
print('- : 3 - 2的值是:', 3 - 2) # 1
#  * 表示两个元素相乘
print('* : 2 * 3的值是:', 2 * 3) # 6
#  / 表示两个元素相除
print('+ : 4 / 2的值是:', 4 / 2) # 2
#  // 表示两个数字取整除
print('// : 9 // 2的值是:', 9 // 2) # 4 .... 1
# % 数字之间取余数
print('% : 9 % 2的值是：' ,9 % 2) # 4 .... 1
# **指数
print("**: 2 ** 4的值是:", 2 ** 4) # 16
